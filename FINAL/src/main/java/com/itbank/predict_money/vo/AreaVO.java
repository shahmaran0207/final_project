package com.itbank.predict_money.vo;


public class AreaVO {
	private int month, food, night, money;
	private String area, goal, nightplace, shop;
    
	
	public String getShop() {
		return shop;
	}

	public void setShop(String shop) {
		this.shop = shop;
	}

	public int getMoney() {
		return money;
	}

	public void setMoney(int money) {
		this.money = money;
	}

	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}

	public String getGoal() {
		return goal;
	}

	public void setGoal(String goal) {
		this.goal = goal;
	}

	public int getMonth() {
		return month;
	}

	public void setMonth(int month) {
		this.month = month;
	}

	public int getNight() {
		return night;
	}

	public void setNight(int night) {
		this.night = night;
	}

	public String getNightplace() {
		return nightplace;
	}

	public void setNightplace(String nightplace) {
		this.nightplace = nightplace;
	}

	public int getFood() {
		return food;
	}

	public void setFood(int food) {
		this.food = food;
	} 
	
	
}